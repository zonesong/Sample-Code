%
% description: 对数据进行标准化处理
%
% Input:       A: 样本矩阵（默认样本向量为列向量）
%
% Output:      A: 标准化之后的样本矩阵
%
% expect:      数据标准化有多种方式，后续完善添加
%              这里默认的是PCA的处理方式
%
% author:      zones
% =========================================================

function A = normalization(A)
A = (A-mean(A,2))./sqrt(sum((A-mean(A,2)).^2,2));
end